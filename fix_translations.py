with open('index.html', 'r', encoding='utf-8') as f:
    content = f.read()

changes = []

# ── 1. Inject EN keys ──────────────────────────────────────────
if 'cap_ba_label: "What' in content:
    print("EN keys already exist — skipping.")
else:
    # Find end of "en": { ... } block — look for sys_op key which is the last en key
    marker_en = 'sys_op: "Operational"'
    if marker_en in content:
        pos = content.index(marker_en) + len(marker_en)
        content = content[:pos] + ',' + EN_KEYS + content[pos:]
        changes.append("✅ EN translation keys injected")
    else:
        print("⚠️  Could not find EN anchor (sys_op: Operational) — trying alternate...")
        # Try finding closing of en block
        marker_en2 = '"en":'
        pos2 = content.find(marker_en2)
        if pos2 != -1:
            # find matching closing brace
            depth = 0
            i = pos2
            while i < len(content):
                if content[i] == '{': depth += 1
                elif content[i] == '}':
                    depth -= 1
                    if depth == 0:
                        content = content[:i] + ',' + EN_KEYS + '\n            ' + content[i:]
                        changes.append("✅ EN translation keys injected (alternate method)")
                        break
                i += 1

# ── 2. Inject AR keys ──────────────────────────────────────────
